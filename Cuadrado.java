public class Cuadrado{
    public static void main(String[] args) {
        //Cuadrado
        System.out.println("Cudrado");

        int altura = 10;
        for(int i =0; i<altura;i++){
            System.out.println("");
            for(int j =0; j<altura;j++){
                System.out.print("*");
            }
        }
        
    }
}