public class Triangulo {
    public static void main(String[] args) {
        //Triangulo
        System.out.println("Triangulo");
        int altura = 6;
        for(int i = 0; i<altura;i++){
            System.out.println("");
        
            for(int j = 0; j<2*i-1;j++){
                System.out.print("*");
            }
        }
        
    }
}
